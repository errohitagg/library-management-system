    private boolean check_data()
    {
        boolean return_value=true;
        JTextField temp_reference_no;
        
        if(jTextField1.getText() == null || jTextField1.getText().compareTo("") == 0)
        {
            jLabel2.setText("<html><font color='red'>ISBN No.");
            return_value = false;
        }
        else
            jLabel2.setText("ISBN No.");

        if(jTextField2.getText() == null || jTextField2.getText().compareTo("") == 0)
        {
            jLabel3.setText("<html><font color='red'>Title");
            return_value = false;
        }
        else
            jLabel3.setText("Title");

        if(jTextField3.getText() == null || jTextField3.getText().compareTo("") == 0)
        {
            jLabel4.setText("<html><font color='red'>Author");
            return_value = false;
        }
        else
            jLabel4.setText("Author");

        if(jTextField4.getText() == null || jTextField4.getText().compareTo("") == 0)
        {
            jLabel5.setText("<html><font color='red'>Publisher");
            return_value = false;
        }
        else
            jLabel5.setText("Publisher");

        if(jTextField5.getText() == null || jTextField5.getText().compareTo("") == 0)
        {
            jLabel6.setText("<html><font color='red'>Price");
            return_value = false;
        }
        else
            jLabel6.setText("Price");

        for(int i = 0; i < book_count; i++)
        {
            temp_reference_no = reference_no.get(i);
            total_books--;
            if(temp_reference_no.getText() == null || temp_reference_no.getText().compareTo("") == 0)
            {
                temp_reference_no = reference_no.remove(i);
                incr_value-=35;

                jButton2.setLocation(150, 250 + incr_value);
                jButton3.setLocation(450, 250 + incr_value);
                java.awt.Dimension screenSize = java.awt.Toolkit.getDefaultToolkit().getScreenSize();
                setBounds((screenSize.width-700)/2, (screenSize.height-(325+incr_value))/2, 700, 325+incr_value);

                book_count--;
                i--;
                this.remove(temp_reference_no);
                
                for(int j=1; j <= total_books; j++)
                {
                    temp_reference_no = reference_no.get(j+i);
                    double y = temp_reference_no.getLocation().getY();
                    temp_reference_no.setLocation(250, (int)(y-35));
                }
                jButton1.setBounds(575, 190 + incr_value, 65, 29);
                this.validate();
                return_value = false;
            }
        }
        total_books = book_count;

        return(return_value);
    }

