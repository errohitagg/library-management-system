    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        if(check_data() == true)
        {
            isbn_no = jTextField1.getText();
            author = jTextField3.getText();
            publisher = jTextField4.getText();;
            price = Integer.parseInt(jTextField5.getText());
            title = jTextField2.getText();

            if(query_class.check_book() == false)
            {
                query_class.insert_values();
                reference_no.removeAll(reference_no);
                this.dispose();
                if(ServerDetails.invoker == Invoker.ADMINISTRATOR)
                {
                    AdministratorPage admin_page = new AdministratorPage();
                    admin_page.show();
                }
                else if(ServerDetails.invoker == Invoker.LIBRARIAN)
                {
                    LibrarianPage librarian_page = new LibrarianPage();
                    librarian_page.show();
                }
                else if(ServerDetails.invoker == Invoker.EMPLOYEE)
                {
                    EmployeePage employee_page = new EmployeePage();
                    employee_page.show();
                }
                else
                    System.exit(0);
            }
            else
                JOptionPane.showMessageDialog(this, "The Book:" + isbn_no + " already exists in the database.", "Duplicate Book", JOptionPane.WARNING_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(this, "Correct the highlighted items before proceeding", "Incorrect Data", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton2MouseClicked

