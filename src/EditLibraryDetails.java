    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try
        {
            String query = "select * from LibraryDetail";
            Statement execute_query = ServerDetails.server_connection.createStatement();
            ResultSet result_set = execute_query.executeQuery(query);

            if(result_set.next())
            {
                jTextField1.setText(result_set.getString("Name"));
                jTextField2.setText(result_set.getString("SName"));
                jTextField3.setText(result_set.getString("Website"));
                jTextField4.setText(result_set.getString("EMailID"));
                jTextField5.setText(result_set.getString("ContactNo"));
                jTextArea1.setText(result_set.getString("Address"));
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null ,"Couldn't connect to the server.\nCheck the server and restart the Application", "Server Error", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }
    }//GEN-LAST:event_formWindowOpened

    private boolean check_data()
    {
        boolean return_value = true;

        if(jTextField1.getText() == null || jTextField1.getText().compareTo("") == 0)
        {
            jLabel1.setText("<html><font color='red'>Library Name");
            return_value = false;
        }
        else
            jLabel1.setText("Library Name");

        if(jTextField2.getText() == null || jTextField2.getText().compareTo("") == 0)
        {
            jLabel2.setText("<html><font color='red'>Library Name<br><center>(Abbr.)");
            return_value = false;
        }
        else
            jLabel2.setText("<html>Library Name<br><center>(Abbr.)");

        if(jTextArea1.getText() == null || jTextArea1.getText().compareTo("") == 0)
        {
            jLabel3.setText("<html><font color='red'>Address");
            return_value = false;
        }
        else
            jLabel3.setText("Address");

        if(jTextField3.getText() == null || jTextField3.getText().compareTo("") == 0)
        {
            jLabel4.setText("<html><font color='red'>Website");
            return_value = false;
        }
        else
            jLabel4.setText("Website");

        if(jTextField4.getText() == null || jTextField4.getText().compareTo("") == 0)
        {
            jLabel5.setText("<html><font color='red'>E-Mail ID");
            return_value = false;
        }
        else
            jLabel5.setText("E-Mail ID");

        if(jTextField5.getText() == null || jTextField5.getText().compareTo("") == 0)
        {
            jLabel5.setText("<html><font color='red'>Phone No.");
            return_value = false;
        }
        else
            jLabel5.setText("Phoen No.");

        return(return_value);
    }

