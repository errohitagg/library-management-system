    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(check_data() == true)
        {
            String library_name, address, website, email_id, phone_no, query;
            library_name = jTextField1.getText();
            ServerDetails.library_name = jTextField2.getText();
            address = jTextArea1.getText();
            website = jTextField3.getText();
            email_id = jTextField4.getText();
            phone_no = jTextField5.getText();

            try
            {
                if(ServerDetails.database == Database.MSACCESS)
                {
                }
                else if(ServerDetails.database == Database.SQLSERVER)
                {
                    query = "truncate table LibraryDetail";
                    Statement execute_query = ServerDetails.server_connection.createStatement();
                    execute_query.executeUpdate(query);

                    query = "insert into LibraryDetail (Name, SName, Address, ContactNo, EMailID, Website) values (?,?,?,?,?,?)";
                    PreparedStatement execute_query_ = ServerDetails.server_connection.prepareStatement(query);
                    execute_query_.setString(1, library_name);
                    execute_query_.setString(2, ServerDetails.library_name);
                    execute_query_.setString(3, address);
                    execute_query_.setString(4, phone_no);
                    execute_query_.setString(5, email_id);
                    execute_query_.setString(6, website);
                    execute_query_.executeUpdate();
                    execute_query.close();
                }                 
                else if(ServerDetails.database == Database.MYSQL)
                {
                    query = "truncate table LibraryDetail";
                    Statement execute_query = ServerDetails.server_connection.createStatement();
                    execute_query.executeUpdate(query);

                    query = "insert into LibraryDetail (Name, SName, Address, ContactNo, EMailID, Website) values (?,?,?,?,?,?)";
                    PreparedStatement execute_query_ = ServerDetails.server_connection.prepareStatement(query);
                    execute_query_.setString(1, library_name);
                    execute_query_.setString(2, ServerDetails.library_name);
                    execute_query_.setString(3, address);
                    execute_query_.setString(4, phone_no);
                    execute_query_.setString(5, email_id);
                    execute_query_.setString(6, website);
                    execute_query_.executeUpdate();
                }                 
                else if(ServerDetails.database == Database.POSTGRESQL)
                {
                }

                this.dispose();
                
                if(ServerDetails.invoker == Invoker.ADMINISTRATOR)
                {
                    AdministratorPage admin_page = new AdministratorPage();
                    admin_page.show();
                }
                else if(ServerDetails.invoker == Invoker.LIBRARIAN)
                {
                    LibrarianPage librarian_page = new LibrarianPage();
                    librarian_page.show();
                }
                else
                    System.exit(0);
            } 
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null ,"Couldn't connect to the server.\nCheck the server and restart the Application" +e, "Server Error", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
        }
        else
            JOptionPane.showMessageDialog(this, "Correct the highlighted items before proceeding", "Incorrect Data", JOptionPane.WARNING_MESSAGE);
}//GEN-LAST:event_jButton1MouseClicked

