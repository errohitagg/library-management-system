    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(check_data() == true)
        {
            if(jComboBox1.getSelectedItem().toString().compareTo("Microsoft Access") == 0)
                database = Database.MSACCESS;
            else if(jComboBox1.getSelectedItem().toString().compareTo("Microsoft SQL Server") == 0)
                database = Database.SQLSERVER;
            else if(jComboBox1.getSelectedItem().toString().compareTo("MySQL") == 0)
                database = Database.MYSQL;
            else if(jComboBox1.getSelectedItem().toString().compareTo("PostgreSQL") == 0)
                database = Database.POSTGRESQL;
            driver_string = jTextField1.getText();
            dsn_url = jTextField2.getText();
            if(driver_type.compareTo("Type 4") == 0)
            {
                user_name = jTextField3.getText();
                password = jPasswordField1.getText();
            }
            else
            {
                user_name = null;
                password = null;
            }            

            try
            {
                File server_file = new File("C:\\Server File.lms");
                server_file.createNewFile();
                FileWriter write_file = new FileWriter(server_file);
                BufferedWriter write_server_file = new BufferedWriter(write_file);
                write_server_file.write(database.getName());
                write_server_file.newLine();
                write_server_file.write(driver_type);
                write_server_file.newLine();
                write_server_file.write(driver_string);
                write_server_file.newLine();

                if(Connectivity.driver_type == 4 && database_answer == JOptionPane.NO_OPTION && database.getName().equals(database.SQLSERVER.toString()))
                {
                    write_server_file.write(dsn_url + ";databaseName=Library");
                    create_dsn_url = dsn_url;
                    dsn_url += ";databaseName=Library";
                }
                else if(Connectivity.driver_type == 4 && database_answer == JOptionPane.NO_OPTION)
                {
                    write_server_file.write(dsn_url + "/Library");
                    create_dsn_url = dsn_url;
                    dsn_url += "/Library";
                }
                else
                    write_server_file.write(dsn_url);

                write_server_file.newLine();
                if(window_auth == true)
                    write_server_file.write("true");
                else
                    write_server_file.write("false");
                write_server_file.newLine();
                write_server_file.write(user_name);
                write_server_file.newLine();
                write_server_file.write(password);
                write_server_file.close();
                write_file.close();
                this.dispose();

                if(driver_type.compareTo("Type 1") == 0)
                    new ConnectServerType1();

                else if(driver_type.compareTo("Type 4") == 0 && database_answer == JOptionPane.NO_OPTION)
                {
                    switch(jComboBox1.getSelectedIndex())
                    {
                        case 0: new CreateMSSQLDatabase();
                            break;
                        case 1: new CreateMySQLDatabase();
                            break;
                        case 2: new CreatePostgreSQLDatabase();
                            break;
                        default : JOptionPane.showMessageDialog(this, "Database can't be created.\nContact the Database Administrator.", "Database Error", JOptionPane.ERROR_MESSAGE);
                            server_file.delete();
                            break;
                    }
                }

                else if(driver_type.compareTo("Type 4") == 0)
                    new ConnectServerType4();
            }

            catch(IOException e)
            {
                JOptionPane.showMessageDialog(this, "Server File can't be created.\nRestart the application and try again.", "Server File", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
        }

        else
            JOptionPane.showMessageDialog(this, "Correct the highlighted items before proceeding", "Incorrect Data", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton1MouseClicked

