    private boolean check_data()
    {
        boolean return_value = true;

        if(jTextField1.getText() == null || jTextField1.getText().compareTo("") == 0)
        {
            jLabel3.setText("<html><font color='red'>Driver String");
            return_value = false;
        }
        else
            jLabel3.setText("Driver String");

        if(jTextField2.getText() == null || jTextField2.getText().compareTo("") == 0)
        {
            if(Connectivity.driver_type == 1)
                jLabel4.setText("<html><font color='red'>DNS String");
            else if(Connectivity.driver_type == 4)
                jLabel4.setText("<html><font color='red'>Server URL");
            return_value = false;
        }
        else
        {
            if(Connectivity.driver_type == 1)
                jLabel4.setText("DNS String");
            else if(Connectivity.driver_type == 4)
                jLabel4.setText("Server URL");
        }

        if(driver_type.compareTo("Type 4") == 0)
        {
            if(jComboBox1.getSelectedItem().toString().compareTo(Database.SQLSERVER.toString()) == 0)
            {
                if(jRadioButton1.isSelected() == false && jRadioButton2.isSelected() == false)
                {
                    return_value = false;
                }
                else if(jRadioButton2.isSelected())
                {
                    if(jTextField3.getText() == null || jTextField3.getText().isEmpty())
                    {
                        jLabel5.setText("<html><font color='red'>User Name");
                        jLabel5.setSize(jLabel5.getPreferredSize());
                        return_value = false;
                    }
                    else
                    {
                        jLabel5.setText("User Name");
                        jLabel5.setSize(jLabel5.getPreferredSize());
                    }

                    if(jPasswordField1.getText() == null || jPasswordField1.getText().isEmpty())
                    {
                        jLabel6.setText("<html><font color='red'>Password");
                        jLabel6.setSize(jLabel6.getPreferredSize());
                        return_value = false;
                    }
                    else
                    {
                        jLabel6.setText("Password");
                        jLabel6.setSize(jLabel6.getPreferredSize());
                    }
                }
            }
            else
            {
                if(jComboBox1.getSelectedItem().toString().compareTo(Database.MYSQL.toString()) == 0 ||
                    jComboBox1.getSelectedItem().toString().compareTo(Database.POSTGRESQL.toString()) == 0)
                {
                    if(jTextField3.getText() == null || jTextField3.getText().isEmpty())
                    {
                        jLabel5.setText("<html><font color='red'>User Name");
                        jLabel5.setSize(jLabel5.getPreferredSize());
                        return_value = false;
                    }
                    else
                    {
                        jLabel5.setText("User Name");
                        jLabel5.setSize(jLabel5.getPreferredSize());
                    }

                    if(jPasswordField1.getText() == null || jPasswordField1.getText().isEmpty())
                    {
                        jLabel6.setText("<html><font color='red'>Password");
                        jLabel6.setSize(jLabel6.getPreferredSize());
                        return_value = false;
                    }
                    else
                    {
                        jLabel6.setText("Password");
                        jLabel6.setSize(jLabel6.getPreferredSize());
                    }
                }
            }
        }

        return(return_value);
    }  

