    private boolean check_data()
    {
        boolean return_value= true;

        if(jTextField1.getText() == null || jTextField1.getText().compareTo("") == 0)
        {
            jLabel3.setText("<html><font color='red'>Member ID");
            return_value = false;
        }
        else
        {
            jLabel3.setText("Member ID");
        }

        if(jTextField2.getText() == null || jTextField2.getText().compareTo("") == 0)
        {
            jLabel5.setText("<html><font color='red'>User Name");
            return_value = false;
        }
        else
            jLabel5.setText("User Name");

        if(jPasswordField1.getText() == null || jPasswordField1.getText().compareTo("") == 0)
        {
            jLabel6.setText("<html><font color='red'>Password");
            return_value = false;
        }
        else
            jLabel6.setText("<html>Password");

        if(jPasswordField2.getText() == null || jPasswordField2.getText().compareTo("") == 0)
        {
            jLabel7.setText("<html><font color='red'>Confirm Password");
            return_value = false;
        }
        else
            jLabel7.setText("<html>Confirm Password");

        if(jPasswordField2.getText().compareTo(jPasswordField1.getText()) != 0)
        {
            jLabel6.setText("<html><font color='red'>Password");
            jLabel7.setText("<html><font color='red'>Confirm Password");
            return_value = false;
        }
        else if(jPasswordField2.getText().compareTo(jPasswordField1.getText()) == 0 && (jPasswordField1.getText() == null || jPasswordField1.getText().compareTo("") != 0 && jPasswordField2.getText() == null || jPasswordField2.getText().compareTo("") != 0))
        {
            jLabel6.setText("Password");
            jLabel7.setText("Confirm Password");
        }

        return(return_value);
    }

