    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(check_data() == true)
        {
            unique_no = jTextField1.getText();
            user_name = jTextField2.getText();
            password = jPasswordField1.getText();
            confirm_password = jPasswordField2.getText();
            
            if(!(query_class.check_unique_id_exists()))
                JOptionPane.showMessageDialog(this, unique_no + " doesn't exists in the database.", "Invalid Identification", JOptionPane.WARNING_MESSAGE);
            else
            {
                if(!(query_class.check_unique_id()))
                {
                    if(!(query_class.check_user_name()))
                    {
                        query_class.insert_values();
                        JOptionPane.showMessageDialog(this, "User with User Name:" + user_name + " has been created.", "User Created", JOptionPane.INFORMATION_MESSAGE);
                        this.dispose();
                        Login login = new Login();
                        login.show();
                    }
                    else
                        JOptionPane.showMessageDialog(this, "User Name:" + user_name + " already exists in the database.", "Incorrect User Name", JOptionPane.WARNING_MESSAGE);                        
                }
                else
                    JOptionPane.showMessageDialog(this, "User:" + unique_no + " already has a User Login ID", "Duplicate Entry", JOptionPane.WARNING_MESSAGE);
            }
        }
        else
           JOptionPane.showMessageDialog(this, "Correct the highlighted items before proceeding", "Incorrect Data", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton1MouseClicked

