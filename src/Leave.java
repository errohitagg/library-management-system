    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(check_data() == true)
        {
            employee_code = jTextField1.getText();
            leave_date.clear();
            leave_date.set(Integer.parseInt(jComboBox3.getSelectedItem().toString()), jComboBox2.getSelectedIndex(), Integer.parseInt(jComboBox1.getSelectedItem().toString()));
            leave_details = jTextArea1.getText();
            if(query_class.check_employee() == true)
            {
                query_class.insert_values();
                this.dispose();
            }
            else
                JOptionPane.showMessageDialog(this, "Employee:" + employee_code + " doesn't exists in the database.", "Invalid Employee", JOptionPane.WARNING_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(this, "Correct the highlighted items before proceeding", "Incorrect Data", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton1MouseClicked

