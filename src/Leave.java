    private boolean check_data()
    {
        boolean return_value=true;

        if(jTextField1.getText() == null || jTextField1.getText().compareTo("") == 0)
        {
            jLabel2.setText("<html><font color='red'>Employee ID");
            return_value = false;
        }
        else
            jLabel2.setText("Employee ID");

        if(jTextArea1.getText() == null || jTextArea1.getText().compareTo("") == 0)
        {
            jLabel4.setText("<html><font color='red'>Leave Details");
            return_value = false;
        }
        else
            jLabel4.setText("Leave Details");

        if(check_date() == false)
        {
            jLabel3.setText("<html><font color='red'>Leave Date");
            return_value = false;
        }
        else
            jLabel3.setText("Leave Date");

        return(return_value);
    }

    private boolean check_date()
    {
        boolean return_value = true;
        String date = (jComboBox2.getSelectedIndex()+1) + "/" + jComboBox1.getSelectedItem().toString() + "/" + jComboBox3.getSelectedItem().toString();

        try
        {
            DateFormat date_format = DateFormat.getDateInstance(DateFormat.SHORT);
            date_format.setLenient(false);
            Date temp_date = date_format.parse(date);
        }
        catch(ParseException e)
        {
            return_value = false;
        }

        return(return_value);
    }

