    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(check_data() == true)
        {
            String college_name, address, website, email_id, phone_no, query;
            college_name = jTextField1.getText();
            ServerDetails.library_name = jTextField2.getText();
            address = jTextArea1.getText();
            website = jTextField3.getText();
            email_id = jTextField4.getText();
            phone_no = jTextField5.getText();

            try
            {
                if(ServerDetails.database == Database.SQLSERVER)
                {
                    query = "insert into LibraryDetail (Name, SName, Address, ContactNo, EMailID, Website) values (?,?,?,?,?,?)";
                    PreparedStatement execute_query = ServerDetails.server_connection.prepareStatement(query);
                    execute_query.setString(1, college_name);
                    execute_query.setString(2, ServerDetails.library_name);
                    execute_query.setString(3, address);
                    execute_query.setString(4, phone_no);
                    execute_query.setString(5, email_id);
                    execute_query.setString(6, website);
                    execute_query.executeUpdate();
                    execute_query.close();
                }
                else if(ServerDetails.database == Database.MYSQL)
                {
                    query = "insert into LibraryDetail (Name, SName, Address, ContactNo, EMailID, Website) values (?,?,?,?,?,?)";
                    PreparedStatement execute_query = ServerDetails.server_connection.prepareStatement(query);
                    execute_query.setString(1, college_name);
                    execute_query.setString(2, ServerDetails.library_name);
                    execute_query.setString(3, address);
                    execute_query.setString(4, phone_no);
                    execute_query.setString(5, email_id);
                    execute_query.setString(6, website);
                    execute_query.executeUpdate();
                    execute_query.close();
                }
                else if(ServerDetails.database == Database.POSTGRESQL)
                {
                }               
                this.dispose();
                AdministratorPage admin_page = new AdministratorPage();
                admin_page.show();
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null ,"Couldn't connect to the server.\nCheck the server and restart the Application", "Server Error", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
        }
        else
            JOptionPane.showMessageDialog(this, "Correct the highlighted items before proceeding", "Incorrect Data", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton1MouseClicked

