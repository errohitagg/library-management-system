    private boolean check_data()
    {
        boolean return_value = true;
        
        if(jTextField1.getText() == null || jTextField1.getText().compareTo("") == 0)
        {
            jLabel1.setText("<html><font color='red'>Library Name");
            return_value = false;
        }
        else
            jLabel1.setText("Library Name");

        if(jTextField2.getText() == null || jTextField2.getText().compareTo("") == 0)
        {
            jLabel2.setText("<html><font color='red'>Library Name<br><center>(Abbr.)");
            return_value = false;
        }
        else
            jLabel2.setText("<html>Library Name<br><center>(Abbr.)");

        if(jTextArea1.getText() == null || jTextArea1.getText().compareTo("") == 0)
        {
            jLabel3.setText("<html><font color='red'>Address");
            return_value = false;
        }
        else
            jLabel3.setText("Address");

        if(jTextField3.getText() == null || jTextField3.getText().compareTo("") == 0)
        {
            jLabel4.setText("<html><font color='red'>Website");
            return_value = false;
        }
        else
            jLabel4.setText("Website");

        if(jTextField4.getText() == null || jTextField4.getText().compareTo("") == 0)
        {
            jLabel5.setText("<html><font color='red'>E-Mail ID");
            return_value = false;
        }
        else
            jLabel5.setText("E-Mail ID");

        if(jTextField5.getText() == null || jTextField5.getText().compareTo("") == 0)
        {
            jLabel6.setText("<html><font color='red'>Phone No.");
            return_value = false;
        }
        else
            jLabel6.setText("Phone No.");
                
        return(return_value);
    }

